package ders13_NestedForLoop_MethodOlusturma;

public class C04_NestedForLoop {

    public static void main(String[] args) {

        /*
        Asagidaki sekli yazdiran bir kod hazirlayin
        * * * * * *
        * * * * * *
        * * * * * *
        * * * * * *

         */

        for (int i = 1; i <=4 ; i++) { // asagiya doru kac satir oldugunu yazdirir

            for (int j = 1; j <=6 ; j++) { // her bir satirin ne kadar oldugunu yazdirir

                System.out.print( "* ");

            }

            System.out.println(""); // ic loop bitince alt satira gec

        }

        }
    }

