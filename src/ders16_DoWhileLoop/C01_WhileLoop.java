package ders16_DoWhileLoop;

public class C01_WhileLoop {

    public static void main(String[] args) {


        // 10 11 12 sayilarini toplayan bir whileloop olusturalim

        int sayi = 10;

        int toplam= 0;

        while (sayi<=12){

            toplam+=sayi;

            sayi++;






        }

        System.out.println(toplam);




        }
    }

